    private void btnprivateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprivateActionPerformed
        // TODO add your handling code here:
       DefaultTableModel model=(DefaultTableModel) tblnote.getModel();
       int row = tblnote.getSelectedRow();
       
        
        try {
            if(row == -1){
                        JOptionPane.showMessageDialog(null, "Please Select a Note To Add !");
                        return;
            }
            int[] multiplenote=tblnote.getSelectedRows();
            Connection con = connectionprovider.getCon();
            PreparedStatement pst = con.prepareStatement("update tblnote set privkey=? where noteid=?");
            for(int i=0;i<=multiplenote.length;i++){
            pst.setInt(1, 1);
            pst.setInt(2,nid);
            }
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Note Made Private Successfully ");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error Making private note ");

        }
        
        loadtable();
        tblnote.clearSelection();
        tblcategory.clearSelection();
        lblnote.setText("");
        lblcategory.setText("");
        lblcontent.setText("");
        lbldt.setText("");
        
    }//GEN-LAST:event_btnprivateActionPerformed

