    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        int[] selectedRow = tblnote.getSelectedRows();
        Object category = tblcategory.getSelectedRow();
        if (tblcategory.getSelectedRowCount() == 0 || tblnote.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Add Category or Note First ! ");

        } else {

            try {

                Connection con = connectionprovider.getCon();
                PreparedStatement pst = con.prepareStatement("update tblnote set categoryfk=? where noteid=?");
                for (int data : selectedRow) {
                    pst.setInt(1, cid);
                    pst.setInt(2, data + 1);
                    pst.executeUpdate();
                }
                JOptionPane.showMessageDialog(null, "Note Added To Category ");

            } catch (Exception e) {

                JOptionPane.showMessageDialog(null, "An Error occured while adding note !");
            }
            loadtable();
            lblnote.setText("");
            lblcategory.setText("");
            lblcontent.setText("");
            lbldt.setText("");
        }
    }//GEN-LAST:event_btnaddActionPerformed

