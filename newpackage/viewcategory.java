    private boolean emptytable() {

        if (tblcategory.getRowCount() == 0 || tblnote.getRowCount() == 0) {

            return false;
        } else {
            return true;
        }
    }

    private void loadtable() {

        DefaultTableModel model = (DefaultTableModel) tblnote.getModel();
        model.setRowCount(0);

        try {
            Connection con = connectionprovider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from tblnote where privkey=0");
            while (rs.next()) {

                model.addRow(new Object[]{rs.getInt("noteid"), rs.getString("notetitle"), rs.getString("notecontent"), rs.getString("notedatetime")});
                 
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        DefaultTableModel cmodel = (DefaultTableModel) tblcategory.getModel();
        cmodel.setRowCount(0);
        try {
            Connection con = connectionprovider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from tblcategory");

            while (rs.next()) {

                cmodel.addRow(new Object[]{rs.getInt("categoryid"), rs.getString("categoryname")});

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        tblnote.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);

    }

