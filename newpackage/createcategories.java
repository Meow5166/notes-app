    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if (txtcategory.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Category Name !");

        } else {
            String cname = txtcategory.getText().trim();
            try {
                Connection con = connectionprovider.getCon();
                PreparedStatement pst = con.prepareStatement("insert into tblcategory (categoryname) values(?)");
                pst.setString(1, cname);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Category Created ");
                txtcategory.setText("");
                DefaultTableModel model = (DefaultTableModel) tblcategoryshow.getModel();
                model.setRowCount(0);
                loadcategory();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);

            }

        }

    }//GEN-LAST:event_btnsaveActionPerformed

