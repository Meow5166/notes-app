    private void tblcategoryshowMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblcategoryshowMouseClicked
        // TODO add your handling code here:

        btnsave.setEnabled(false);
        int index = tblcategoryshow.getSelectedRow();

        TableModel model = tblcategoryshow.getModel();
        cid = Integer.parseInt(model.getValueAt(index, 0).toString());
        String categoryname = model.getValueAt(index, 1).toString();
        txtcategory.setText(categoryname);
        btnsave.setEnabled(false);

    }//GEN-LAST:event_tblcategoryshowMouseClicked

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblcategoryshow.getModel();

        try {

            if (tblcategoryshow.getSelectedRow() == -1) {
                cid = 0;
                JOptionPane.showMessageDialog(null, "No Category Selected");

            } else {
                Connection con = connectionprovider.getCon();
                PreparedStatement del_cfk = con.prepareStatement("update tblnote set categoryfk = NULL where categoryfk =?");
                del_cfk.setInt(1, cid);
                del_cfk.executeUpdate();
                PreparedStatement pst = con.prepareStatement("delete from tblcategory where categoryid=?");
                pst.setInt(1, cid);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Category Deleted Successfully !");
                txtcategory.setText("");
                model.setRowCount(0);
                loadcategory();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }


    }//GEN-LAST:event_btndeleteActionPerformed

